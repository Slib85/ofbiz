	public static List<Map<String, Object>> getSearchData(String searchString) {
		List<Map<String, Object>> searchItemList = new ArrayList<>();
		List<String> searchStringList = Arrays.asList(searchString.split(" "));

		for (String categoryName : (List<String>) categoryList) {
			if (UtilValidate.isNotEmpty(((Map<String, Object>) category.get(categoryName)).get("ItemList"))) {
				for (Map<String, Object> categoryItem : ((List<Map<String, Object>>) ((Map<String, Object>) category.get(categoryName)).get("ItemList"))) {
					for (String splitSearchString : searchStringList) {
						if (
								UtilValidate.isNotEmpty(splitSearchString.replace(" ", "")) && (
										"*".equals(searchString) ||
												(UtilValidate.isNotEmpty(categoryItem.get("ProductInfo")) && (
														(UtilValidate.isNotEmpty(((Map<String, Object>) categoryItem.get("ProductInfo")).get("Sku")) && (((String) ((Map<String, Object>) categoryItem.get("ProductInfo")).get("Sku")).toLowerCase()).contains(splitSearchString.toLowerCase())) ||
																(UtilValidate.isNotEmpty(((Map<String, Object>) categoryItem.get("ProductInfo")).get("Title")) && (((String) ((Map<String, Object>) categoryItem.get("ProductInfo")).get("Title")).toLowerCase()).contains(splitSearchString.toLowerCase())) ||
																(UtilValidate.isNotEmpty(((Map<String, Object>) categoryItem.get("ProductInfo")).get("Description")) && (((String) ((Map<String, Object>) categoryItem.get("ProductInfo")).get("Description")).toLowerCase()).contains(splitSearchString.toLowerCase())) ||
																(UtilValidate.isNotEmpty(((Map<String, Object>) categoryItem.get("ProductInfo")).get("Dimensions / Finished Folded Size")) && (((String) ((Map<String, Object>) categoryItem.get("ProductInfo")).get("Dimensions / Finished Folded Size")).toLowerCase()).contains(splitSearchString.toLowerCase())) ||
																(UtilValidate.isNotEmpty(((Map<String, Object>) categoryItem.get("ProductInfo")).get("Material Type")) && (((String) ((Map<String, Object>) categoryItem.get("ProductInfo")).get("Material Type")).toLowerCase()).contains(splitSearchString.toLowerCase())) ||
																(UtilValidate.isNotEmpty(((Map<String, Object>) categoryItem.get("ProductInfo")).get("Minimum Quantity")) && (((String) ((Map<String, Object>) categoryItem.get("ProductInfo")).get("Minimum Quantity")).toLowerCase()).contains(splitSearchString.toLowerCase()))
												))
								)
								) {
							boolean existsInList = false;

							for (Map<String, Object> searchItem : (List<Map<String, Object>>) searchItemList) {
								if (((String) searchItem.get("Style Code")).equals((String) categoryItem.get("Style Code"))) {
									existsInList = true;
								}
							}

							if (!existsInList) {
								searchItemList.add(categoryItem);
								break;
							}
						}
					}
				}
			}
		}

		return searchItemList;
	}
